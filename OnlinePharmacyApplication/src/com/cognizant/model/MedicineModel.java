package com.cognizant.model;

public class MedicineModel {
	private String medName;
	private String medType;
	private String unitValue;
	private String medPrice;
	private String unit;
	private String medExpiryDate;
	private String inStock;
	
	
	public String getMedName() {
		return medName;
	}
	
	public void setMedName(String medName) {
		this.medName = medName;
	}
	
	public String getMedType() {
		return medType;
	}
	
	public void setMedType(String medType) {
		this.medType = medType;
	}
	
	public String getUnitValue() {
		return unitValue;
	}
	
	public void setUnitValue(String unitValue) {
		this.unitValue = unitValue;
	}
	
	public String getMedPrice() {
		return medPrice;
	}
	
	public void setMedPrice(String medPrice) {
		this.medPrice = medPrice;
	}
	
	public String getUnit() {
		return unit;
	}
	
	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	public String getMedExpiryDate() {
		return medExpiryDate;
	}
	
	public void setMedExpiryDate(String medExpiryDate) {
		this.medExpiryDate = medExpiryDate;
	}
	
	public String getInStock() {
		return inStock;
	}
	
	public void setInStock(String inStock) {
		this.inStock = inStock;
	}
}
